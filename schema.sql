CREATE OR REPLACE FUNCTION "public"."expire_orders"() RETURNS "void"
    LANGUAGE "plpgsql" SECURITY DEFINER
    AS $$
BEGIN
  UPDATE orders
  SET status = 'expired',
      updated_at = NOW()
  WHERE status = 'pending'
    AND expiration_time IS NOT NULL
    AND expiration_time < NOW();
END;
$$;


ALTER FUNCTION "public"."expire_orders"() OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."generate_referral_code"() RETURNS "text"
    LANGUAGE "plpgsql" SECURITY DEFINER
    SET "search_path" TO 'public', 'pg_temp'
    AS $$
DECLARE
  code TEXT;
  exists BOOLEAN;
BEGIN
  LOOP
    -- Generate random 8-character code
    code := upper(substring(md5(random()::text) from 1 for 8));
    
    -- Check if code already exists
    SELECT EXISTS(SELECT 1 FROM profiles WHERE referral_code = code) INTO exists;
    
    IF NOT exists THEN
      RETURN code;
    END IF;
  END LOOP;
END;
$$;


ALTER FUNCTION "public"."generate_referral_code"() OWNER TO "postgres";


