CREATE OR REPLACE FUNCTION "public"."process_referral_credit"("p_referrer_id" "uuid", "p_referred_id" "uuid", "p_credit_amount" numeric) RETURNS boolean
    LANGUAGE "plpgsql"
    AS $$
DECLARE
  v_referral_id UUID;
  v_new_balance NUMERIC;
BEGIN
  -- Update referral status
  UPDATE referrals
  SET status = 'credited',
      credit_amount = p_credit_amount,
      credited_at = now()
  WHERE referrer_id = p_referrer_id 
    AND referred_id = p_referred_id
    AND status = 'completed'
  RETURNING id INTO v_referral_id;
  
  IF v_referral_id IS NULL THEN
    RETURN FALSE;
  END IF;
  
  -- Update referrer's credits and total referrals
  UPDATE profiles
  SET referral_credits = referral_credits + p_credit_amount,
      total_referrals = total_referrals + 1
  WHERE id = p_referrer_id
  RETURNING referral_credits INTO v_new_balance;
  
  -- Create wallet transaction
  INSERT INTO wallet_transactions (
    profile_id,
    transaction_type,
    amount,
    balance_after,
    description,
    reference_id
  ) VALUES (
    p_referrer_id,
    'referral_credit',
    p_credit_amount,
    v_new_balance,
    'Referral credit for successful referral',
    v_referral_id
  );
  
  RETURN TRUE;
END;
$$;


ALTER FUNCTION "public"."process_referral_credit"("p_referrer_id" "uuid", "p_referred_id" "uuid", "p_credit_amount" numeric) OWNER TO "postgres";

